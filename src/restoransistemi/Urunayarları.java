    public ArrayList<urunler> urunList(){
            ArrayList<urunler> urunList= new ArrayList<>();
         try{
            Class.forName(sqlayar.Driver);
            Connection con=DriverManager.getConnection(sqlayar.url, sqlayar.user, sqlayar.pass);
            String sqlsorgu="Select * from ürünler ";  
          
            Statement st=con.createStatement();
            ResultSet rs=st.executeQuery(sqlsorgu);
         
            urunler urun;
          
            while(rs.next()){
                urun=new urunler(rs.getString("urunbilgisi"),rs.getInt("fiyat"));
           urunList.add(urun);
            }

        }catch(Exception e){
             JOptionPane.showMessageDialog(this,e.getMessage());
        }
         return urunList;
    }
        public void show_urun(){
        ArrayList<urunler> list= urunList();
        DefaultTableModel model= (DefaultTableModel)uruntbl.getModel();
        Object[] row= new Object[2];
        for(int i=0;i<list.size();i++){
            row[0]=list.get(i).geturun();
            row[1]=list.get(i).gethesap();
            model.addRow(row);
        }
    }
    
