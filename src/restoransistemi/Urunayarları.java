    private void eklebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eklebtnActionPerformed
 try{
            Class.forName(sqlayar.Driver);
            Connection con=DriverManager.getConnection(sqlayar.url, sqlayar.user, sqlayar.pass);
            String sql="insert into ürünler"+"(urunbilgisi,fiyat)"+"values(?,?)";
            
            PreparedStatement pst=con.prepareStatement(sql);
       
             pst.setString(1,uruntxt.getText());
              pst.setString(2,fiyattxt.getText());
            
              
              String masa=uruntxt.getText();
              String fiyat=fiyattxt.getText();
              if(masa.equals("")|| fiyat.equals("") ){
                   JOptionPane.showMessageDialog(this,"Acıklamayı yada fiyatı boş bırakamazsın. ");
              }
              else{
                  pst.executeUpdate();
                    DefaultTableModel model= (DefaultTableModel)uruntbl.getModel();
            
             model.setNumRows(0);
             
              show_urun();
              uruntxt.setText("");
              fiyattxt.setText("");
              JOptionPane.showMessageDialog(this,"Yeni Ürün oluşturuldu.");}
        }catch(Exception e){
             JOptionPane.showMessageDialog(this,e.getMessage());
        }
        
    }//GEN-LAST:event_eklebtnActionPerformed

